# 2개의 숫자를 입력 받아 더한 결과와 곱한 결과를 출력한다.
# >>> 첫번째 숫자를 입력하시오: 5
# >>> 두번째 숫자를 입력하시오: 53
#  더한 결과는 58 이고, 곱한 결과는 265 입니다.
num1 = input("첫번째 숫자를 입력하시오: ")  # 첫번째 숫자 input
num2 = input("두번째 숫자를 입력하시오: ")  # 두번째 숫자 input
print("더한 결과는", int(num1) + int(num2), "이고, 곱한 결과는", int(num1) * int(num2), "입니다.")  # 결과 출력
